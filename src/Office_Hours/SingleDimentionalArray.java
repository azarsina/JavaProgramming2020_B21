package Office_Hours;

import java.util.Arrays;

public class SingleDimentionalArray {
    public static void main(String[] args) {
        int[] scores = new int[5];

        scores [0] = 10;
        scores [1] = 20;
        scores [2] = 50;
        scores [3] = 30;
        scores [4] = 40;

        System.out.println( Arrays.toString(scores) );
    }
}
